package day17_loops;

public class DoWhileExample {
    public static void main(String[] args) {
        // While LOOP
        int m = 1;
        while (m<=10){

            System.out.println("While Lopp "+m);
            m++;
        }

        // flow
        //   1st chek condition
        //   if true execution code block


        System.out.println("==================================");
        int n = 1;
        do {
            System.out.println("Do hwile loop "+n);
            n++;
        }while (n<=10);
        // flow
        // 1 execute code block of DO
        //  then chek condition
    }
}
